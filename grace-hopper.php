		             	<h3>Main Contribution</h3>

		             		<figure>
		             			<img src="images/mark-i.jpg" alt="Harvard Mark I Computer">
		             		</figure>

		             		<p>She was one of the first programmers of the Harvard Mark I computer in 1944, invented the first compiler for a computer programming language, and was one of those who popularized the idea of machine-independent programming languages which led to the development of COBOL, one of the first high-level programming languages.</p>

		             	<h3>Education</h3>

		             		<p>She obtained her bachelor's in mathematics and physics from Vassar College in 1928. She had applied for early admission at age 16 and was denied, but was then admitted the following year. She then got her master's degree from Yale University in 1930. She also got her Ph.D. in mathematics from Yale in 1934.</p>

		             	<h3>Career</h3>

		             		<p>Hopper began teaching mathematics at Vassar in 1931, and was promoted to associate professor in 1941.</p>
		             		<p>In 1943, during World War II, Hopper obtained a leave of absence from Vassar and was sworn into the United States Navy Reserve, one of many women to volunteer to serve in the WAVES. She had to get an exemption to enlist; she was 15 pounds (6.8 kg) below the Navy minimum weight of 120 pounds (54 kg). She reported in December and trained at the Naval Reserve Midshipmen's School at Smith College in Northampton, Massachusetts. Hopper graduated first in her class in 1944, and was assigned to the Bureau of Ships Computation Project at Harvard University as a lieutenant, junior grade. She served on the Mark I computer programming staff headed by Howard H. Aiken. Hopper and Aiken coauthored three papers on the Mark I, also known as the Automatic Sequence Controlled Calculator. Hopper's request to transfer to the regular Navy at the end of the war was declined due to her age (38). She continued to serve in the Navy Reserve. Hopper remained at the Harvard Computation Lab until 1949, turning down a full professorship at Vassar in favor of working as a research fellow under a Navy contract at Harvard.</p>
		             		<p>In 1949, Hopper became an employee of the Eckert–Mauchly Computer Corporation as a senior mathematician and joined the team developing the UNIVAC I. In the early 1950s, the company was taken over by the Remington Rand corporation, and it was while she was working for them that her original compiler work was done. The compiler was known as the A compiler and its first version was A-0. In 1952 she had an operational compiler. "Nobody believed that," she said. "I had a running compiler and nobody would touch it. They told me computers could only do arithmetic." In 1954 Hopper was named the company's first director of automatic programming, and her department released some of the first compiler-based programming languages, including MATH-MATIC and FLOW-MATIC.</p>
		             		<p>In the spring of 1959, a two-day conference known as the Conference on Data Systems Languages (CODASYL) brought together computer experts from industry and government. Hopper served as a technical consultant to the committee, and many of her former employees served on the short-term committee that defined the new language COBOL (an acronym for COmmon Business-Oriented Language). The new language extended Hopper's FLOW-MATIC language with some ideas from the IBM equivalent, COMTRAN. From 1967 to 1977, Hopper served as the director of the Navy Programming Languages Group in the Navy's Office of Information Systems Planning and was promoted to the rank of Captain in 1973. She developed validation software for COBOL and its compiler as part of a COBOL standardization program for the entire Navy. In the 1970s she developed the implementation of standards for testing computer systems and components, most significantly for early programming languages such as FORTRAN and COBOL.</p>
		             		<p>Hopper retired from the Naval Reserve at age 60, but she was recalled to active duty in August 1967 for a six-month period that turned into an indefinite assignment. She again retired in 1971, but was asked to return to active duty again in 1972. She was promoted to Captain in 1973 by Admiral Elmo R. Zumwalt, Jr. After March of 1938 she was promoted to Commodore (Admiral, O-7) by special Presidential appointment, the rank later renamed to Rear Admiral (Lower Half). She retired (involuntarily) from the Navy on August 14, 1986.</p>
		             		<p>Later, she was hired as a senior consultant to Digital Equipment Corporation, a position she retained until her death in 1992 at age 85. Her primary activity in this capacity was as a goodwill ambassador, lecturing widely on the early days of computers, her career, and on efforts that computer vendors could take to make life easier for their users.</p>

		             	<h3>Personal Life</h3>

		             		<p>Hopper was born in New York City. She was the oldest in a family of three children. Her parents, Walter Fletcher Murray and Mary Campbell Van Horne, were of Dutch and Scottish descent, and attended West End Collegiate Church. Her great-grandfather, Alexander Wilson Russell, an admiral in the US Navy, fought in the Battle of Mobile Bay during the Civil War.</p>
		             		<p>She was married to New York University professor Vincent Foster Hopper (1906–76) from 1930 until their divorce in 1945. She did not marry again, but she kept his surname.</p>
