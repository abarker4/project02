					<p>Hamilton joined the Charles Stark Draper Laboratory at MIT, which at the time was working on the Apollo space mission. She eventually led a team credited with developing the software for Apollo and Skylab. Hamilton's team was responsible for developing in-flight software, which included algorithms designed by various senior scientists for the Apollo command module, lunar lander, and the subsequent Skylab. Another part of her team designed and developed the systems software which included the error detection and recovery software such as restarts and the Display Interface Routines (AKA the Priority Displays) which Hamilton designed and developed.</p>
					<p>It was her efforts on the SAGE Project, which was an extension of Project Whirlwind, that made her a candidate for the position at NASA as the lead developer for Apollo flight software. It was started by MIT to create a computer system that could predict weather systems and track their movements through simulators. The SAGE Project was soon developed for military use in anti-aircraft air defense from potential Soviet attacks during the Cold War. </p>

				<h3>Education</h3>

					<p>Hamilton obtained a BA in mathematics with a minor in philosophy from Earlham College in 1958.</p>

				<h3>Career</h3>


					<p>She briefly taught high school mathematics and French upon graduation from Earlham College.In 1960 she took an interim position at MIT to develop software for predicting weather on the LGP-30 and the PDP-1 computers (at Marvin Minsky's Project MAC) for professor Edward Norton Lorenz in the meteorology department.From 1961 to 1963, she worked on the SAGE Project at Lincoln Lab, where she was one of the programmers who wrote software for the first AN/FSQ-7 computer (the XD-1), to search for "unfriendly" aircraft; she also wrote software for the Air Force Cambridge Research Laboratories.</p>
					<p>From 1976 through 1984, Hamilton was the CEO of a company she co-founded called Higher Order Software (HOS) to further develop ideas about error prevention and fault tolerance emerging from her experience at MIT. Hamilton left the company in 1985. In March 1986, she became the founder and CEO of Hamilton Technologies, Inc. in Cambridge, Massachusetts.</p>

				<h3>Personal Life</h3>

					<p>She met her husband James Cox Hamilton while at Earlham College. They married in the late 1950s after Heafield earned her bachelor's degree. They had a daughter together named Lauren, who, later in her life, married the billionaire heir, actor, dancer, and choreographer James Cox Chambers. During the weekends she would often take Lauren to the lab to spend time with her daughter and simultaneously "code away creating routines that would later be added to Apollo's command module computer". Margaret Hamilton and James Cox Hamilton eventually divorced.</p>
