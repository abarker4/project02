		             	<p>Tim Berners-Lee is best known for his invention of the World Wide Web in 1989. He took the hypertext idea and connected it to the Transmission Control Protocol and domain name system ideas in creating the web. He used similar ideas to those underlying the ENQUIRE system to create the World Wide Web, for which he designed and built the first Web browser.</p>

		             	<p>He was honoured as the "Inventor of the World Wide Web" during the 2012 Summer Olympics opening ceremony.</p>

		            <h3>Education</h3>

		            	<p>He studied at The Queen's College, Oxford graduating in 1976 obtaining a first-class bachelor of arts degree in physics.</p>

		            <h3>Career</h3>

		            	<p>After graduation, Berners-Lee worked as an engineer at the telecommunications company Plessey in Poole, Dorset. In 1978, he joined D. G. Nash in Ferndown, Dorset, where he helped create type-setting software for printers. Berners-Lee worked as an independent contractor at CERN from June to December 1980. While at CERN he proposed a project based on the concept of hypertext, to facilitate sharing and updating information among researchers. He built a prototype system named ENQUIRE as a demonstration for this project. He left CERN in late 1980 and went to work at John Poole's Image Computer Systems, Ltd. He, however, returned to CERN as a fellow in 1984. It was during this return to CERN that his creation of the World Wide Web came to be. Tim Berners-Lee started this project because, "...the situation without it was very difficult when I was working at CERN later. Most of the technology involved in the web, like the hypertext, like the Internet, multifont text objects, had all been designed already. I just had to put them together. It was a step of generalising, going to a higher level of abstraction, thinking about all the documentation systems out there as being possibly part of a larger imaginary documentation system."</p>

		            	<figure>
		            		<img src="images/w3c.png" alt="W3C Logo">
		            	</figure>
		            	
		            	<p>In 1994, Berners-Lee founded the W3C at the Massachusetts Institute of Technology. It comprised various companies that were willing to create standards and recommendations to improve the quality of the Web. Berners-Lee made his idea available freely, with no patent and no royalties due. The World Wide Web Consortium decided that its standards should be based on royalty-free technology, so that they easily could be adopted by anyone.</p>
		            	<p> In December 2004, he accepted a chair in computer science at the School of Electronics and Computer Science, University of Southampton, Hampshire, to work on the Semantic Web.</p>
		            	<p>In June 2009, then-British Prime Minister Gordon Brown announced Berners-Lee would work with the UK Government to help make data more open and accessible on the Web, building on the work of the Power of Information Task Force. In November 2009, Berners-Lee launched the World Wide Web Foundation in order to "advance the Web to empower humanity by launching transformative programs that build local capacity to leverage the Web as a medium for positive change." Also, Berners-Lee joined the board of advisors of start-up State.com, based in London, and as of May 2012, Berners-Lee is president of the Open Data Institute.</p>

		            <h3>Personal Life</h3>

		            	<p>Berners-Lee was married to Nancy Carlson in 1990; they had two children and divorced in 2011. In 2014 Berners-Lee married Rosemary Leith at St. James's Palace in London. Leith is director of the World Wide Web Foundation and a fellow at Harvard University's Berkman Center. Previously, she was World Economic Forum Global Agenda Council Chair of the Future of Internet Security and now is on the board of YouGov. Berners-Lee was raised as an Anglican, but in his youth, he turned away from religion. After he became a parent, he became a Unitarian Universalist (UU). He has stated: "Like many people, I had a religious upbringing which I rejected as a teenager... Like many people, I came back to religion when we had children". He and his wife wanted to teach spirituality to his children, and after hearing a Unitarian minister and visiting the UU Church, they opted for it. He is an active member of that church, to which he adheres because he perceives it as a tolerant and liberal belief. He also has recognized the value of other faiths, stating: "I believe that much of the philosophy of life associated with many religions is much more sound than the dogma which comes along with it. So I do respect them."</p>

				
